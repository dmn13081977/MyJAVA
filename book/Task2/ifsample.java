public class ifsample {
	public static void main (String[] args) {
		int x, y;
		x = 10;
		y = 20;
		if (x < y) System.out.println("Значение x меньше y");
		x = x * 2;
		if (x == y) System.out.println("Теперь значение х равно у");
		x = x * 2;
		if (x > y) System.out.println("Теперь значение х больше у");
	}
}
